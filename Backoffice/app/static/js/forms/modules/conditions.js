// Debug utility to check a specific field value
window.debugFieldValue = function(fieldId) {
    debugLog(MODULE_NAME, `=== DEBUG FIELD ${fieldId} ===`);
    const value = getCurrentFieldValue(fieldId);
    debugLog(MODULE_NAME, `getCurrentFieldValue(${fieldId}):`, value);

    // Check all possible checkbox patterns
    const patterns = [
        `indicator_${fieldId}_standard_value`,
        `field_value[${fieldId}]`,
        `dynamic_*_${fieldId}_standard_value`
    ];

    patterns.forEach(pattern => {
        const checkboxes = document.querySelectorAll(`input[name="${pattern}"], input[name*="${fieldId}"][name*="standard_value"]`);
        if (checkboxes.length > 0) {
            debugLog(MODULE_NAME, `Found ${checkboxes.length} checkboxes for pattern "${pattern}"`);
            checkboxes.forEach((cb, i) => {
                debugLog(MODULE_NAME, `  Checkbox ${i}: name="${cb.name}", value="${cb.value}", checked=${cb.checked}`);
            });
        } else {
            debugLog(MODULE_NAME, `No checkboxes found for pattern "${pattern}"`);
        }
    });

    // Check existing data
    if (window.existingData) {
        const dataKey = `field_value[${fieldId}]`;
        debugLog(MODULE_NAME, `existingData["${dataKey}"]:`, window.existingData[dataKey]);
    }

    debugLog(MODULE_NAME, `=== END DEBUG FIELD ${fieldId} ===`);
};
