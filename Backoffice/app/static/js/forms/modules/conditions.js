/**
 * Collect all hidden field IDs and add them to the form for server processing
 */
function collectHiddenFieldsForSubmission() {
    // Collect hidden field IDs for server clearing/validation skipping.
    //
    // IMPORTANT:
    // - Field-level relevance adds `.relevance-hidden` directly on `.form-item-block[data-item-id]`
    // - Section-level relevance adds `.relevance-hidden` on the section container (e.g. `#section-container-...`)
    //   but does NOT add `.relevance-hidden` to each child field. We still must include those child field IDs
    //   so the backend does not enforce required fields that were not shown to the user.
    const hiddenFieldIds = new Set();

    // 1) Field-level relevance-hidden
    document.querySelectorAll('.relevance-hidden[data-item-id]').forEach(field => {
        const fieldId = field.getAttribute('data-item-id');
        if (fieldId) {
            hiddenFieldIds.add(fieldId);
            debugLog(MODULE_NAME, `📋 Collected hidden field for server clearing: ${fieldId}`);
        }
    });

    // 2) Section-level hiding: include all contained form item IDs.
    //
    // Sections can be hidden either by relevance (ideal: `.relevance-hidden`) or temporarily via inline
    // `display:none` during early initialization. We treat "hidden + relevance condition" as hidden-by-relevance,
    // but we must NOT include sections hidden only due to pagination (other pages).
    const getComputedHidden = (el) => {
        try {
            const cs = window.getComputedStyle(el);
            return cs.display === 'none' || cs.visibility === 'hidden';
        } catch (_) {
            return false;
        }
    };

    const currentPageNum =
        (window.__ifrcPagination && typeof window.__ifrcPagination.getCurrentPageNumber === 'function')
            ? window.__ifrcPagination.getCurrentPageNumber()
            : null;

    const hiddenSectionCandidates = document.querySelectorAll('div[id^="section-container-"][data-relevance-condition], div[id^="section-container-"].relevance-hidden');
    hiddenSectionCandidates.forEach(section => {
        const isHiddenByClassOrStyle =
            section.classList.contains('relevance-hidden') ||
            section.classList.contains('hidden') ||
            getComputedHidden(section) ||
            section.style.display === 'none';

        if (!isHiddenByClassOrStyle) return;

        // If pagination is active and this section is on a different page, do NOT treat it as hidden-by-relevance.
        if (Number.isFinite(currentPageNum)) {
            const sectionPageNum = parseInt(section.dataset.pageNumber || '1', 10);
            if (Number.isFinite(sectionPageNum) && sectionPageNum !== currentPageNum) {
                return;
            }
        }

        // Only include the section if it is tied to relevance (has a relevance condition) OR explicitly relevance-hidden.
        const hasRelevanceCondition = section.hasAttribute('data-relevance-condition') || section.classList.contains('relevance-hidden');
        if (!hasRelevanceCondition) return;

        section
            .querySelectorAll('.form-item-block[data-item-id]:not([data-item-type="blank"])')
            .forEach(field => {
                const fieldId = field.getAttribute('data-item-id');
                if (fieldId) hiddenFieldIds.add(fieldId);
            });
    });

    const hiddenFieldIdList = Array.from(hiddenFieldIds);

    // Always ensure the hidden input exists and reflects current state (avoid stale values).
    let hiddenFieldsInput = document.getElementById('hidden_fields_to_clear');
    if (!hiddenFieldsInput) {
        hiddenFieldsInput = document.createElement('input');
        hiddenFieldsInput.type = 'hidden';
        hiddenFieldsInput.name = 'hidden_fields_to_clear';
        hiddenFieldsInput.id = 'hidden_fields_to_clear';
        const form = document.getElementById('focalDataEntryForm');
        if (form) {
            form.appendChild(hiddenFieldsInput);
        }
    }
    if (hiddenFieldsInput) {
        hiddenFieldsInput.value = hiddenFieldIdList.join(',');
    }

    if (hiddenFieldIdList.length > 0) {
        debugLog(MODULE_NAME, `📋 Added ${hiddenFieldIdList.length} hidden field IDs to form submission: ${hiddenFieldIdList.join(', ')}`);
    }
}

